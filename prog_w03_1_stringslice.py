myStr = "Python Tuple" 
print (myStr) 			# Prints complete string 
print (myStr [0]) 		# Prints first character of the string 
print (myStr [3:7]) 	# Prints characters starting from 4rd to 7th 
print (myStr [4:]) 		# Prints string starting from 5th character 
print (myStr * 3) 		# Prints string two times 
print (myStr [4:]) 		# Prints string starting from 5th character 
print (myStr + "is Powerfull") # Prints concatenated string
print (myStr [-1]) 		
print (myStr [-2]) 		
print (myStr [:-2])
print (myStr [1:-2])

print ("Updated String :", myStr[:6] + "String")
print ("Updated String\a ", myStr [:6] + "String") # add an alert
print ("Updated String\b\b\b", myStr [:6] + "String") # add backspace
print ("Updated String\n ", myStr [:6] + "String") # add a new line
print ("Updated String\t ", myStr [:6] + "String") # add tab